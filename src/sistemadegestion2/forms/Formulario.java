    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        Cliente cliente = new Cliente();
        
         cliente.setNombre(jTextName.getText());
        cliente.setApellido(jTextApellido.getText());
        cliente.setEmail(jTexEmail.getText());
        cliente.setTelefono(jTextTelefono.getText());
        
        if (!StringUtils.isEmptyOrWhitespaceOnly(jLabelId.getText())) {
            cliente.setId(jLabelId.getText());
        }
        ClienteDao dao = new ClienteDao();
        dao.guardarOModifircar(cliente);
         
         JOptionPane.showMessageDialog(rootPane, "El nombre se guardo correctamente");
         
         actualizarLista();
         limpiarTxt();
    }//GEN-LAST:event_jBGuardarActionPerformed

    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        int indiceSeleccionado = this.listClientes.getSelectedIndex();
        Cliente cliente = lista.get(indiceSeleccionado);
        ClienteDao dao = new ClienteDao();
        dao.eliminar(cliente);
        JOptionPane.showMessageDialog(rootPane, "El nombre se elimino correctamente");
        actualizarLista();
    }//GEN-LAST:event_jBEliminarActionPerformed

